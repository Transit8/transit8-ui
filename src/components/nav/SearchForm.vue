<template>
  <form class="search-form col-md-6 col-xs-12 col-xs-offset-0" @submit.prevent="submit()">
    <input type="text" class="form-control" placeholder="Search" v-model="queryString">
    <button type="submit"><span class="icon-search"></span></button>
  </form>
</template>

<script>
// noinspection JSUnusedGlobalSymbols
export default {
  name: 'SearchForm',
  data () {
    return {
      queryString: null,
    }
  },
  methods: {
    submit () {
      if (this.queryString) {
        this.$emit('submit', this.queryString)
      }
    }
  }
}
</script>
